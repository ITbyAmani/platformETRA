const FEATURED_SLUG = 'efficient-learning-path-2026'

export function getFeaturedPost(): BlogPost {
  return BLOG_POSTS.find((p) => p.slug === FEATURED_SLUG) ?? BLOG_POSTS[0]
}

export function getPostBySlug(slug: string | undefined): BlogPost | undefined {
  if (!slug) return undefined
  return BLOG_POSTS.find((p) => p.slug === slug)
}

export function getRelatedPosts(current: BlogPost, limit = 3): BlogPost[] {
  return BLOG_POSTS.filter((p) => p.slug !== current.slug && p.topic === current.topic).slice(0, limit)
}
